    private Destination checkNewDestination() {
        Destination newDestination;
        int destinationID = destinationManager.getNextDestinationID();
        // 9000 IDs is the maximum, which can be stored
        if (destinationID > 9000) {
            JOptionPane.showMessageDialog(this, "Maximum Limit of destinations!", "Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        String dName = textNameDestination.getText().trim();
        String dType = textTypeDestination.getText().trim();
        Double dCost;
        textNameDestination.setBackground(new Color(255, 255, 255));
        textTypeDestination.setBackground(new Color(255, 255, 255));
        textCostDestination.setBackground(new Color(255, 255, 255));

        if (!isValidDName(dName)) {
            JOptionPane.showMessageDialog(userInterface, "Only letters are allowed, and two consecutive spaces are not allowed.\nAnd/Or incorrect characters", "Invalid Name!", JOptionPane.ERROR_MESSAGE);
            textNameDestination.setBackground(new Color(255, 102, 102));
            return null;
        }

        if (!isValidDType(dType)) {
            JOptionPane.showMessageDialog(userInterface, "Only letters are allowed, and two consecutive spaces are not allowed.\nAnd/Or incorrect characters", "Invalid Type!", JOptionPane.ERROR_MESSAGE);
            textTypeDestination.setBackground(new Color(255, 102, 102));
            return null;
        }

        try {
            dCost = Double.valueOf(textCostDestination.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "The cost field should exclusively consist of numerical values!\nPlease correct", "Invalid Cost!", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (dCost <= 0) {
            JOptionPane.showMessageDialog(this, "Cost is too low!\nEnter a positive value under 10000!", "Invalid Cost!", JOptionPane.ERROR_MESSAGE);
            return null;
        } else if (dCost > 10000) {
            JOptionPane.showMessageDialog(this, "Cost is too high!\nEnter a positive value under 10000!", "Invalid Cost!", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        newDestination = new Destination(destinationID, dName, dType, dCost);
        return newDestination;
    }

    private boolean isValidDName(String dName) {
        return dName.matches("[A-Za-z]+(\\s[A-Za-z]+)*") && dName.length() >= 2 && dName.length() <= 20;
    }

    private boolean isValidDType(String dType) {
        return dType.matches("[A-Za-z]+(\\s[A-Za-z]+)*") && dType.length() >= 2 && dType.length() <= 20;
    }

