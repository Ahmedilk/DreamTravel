    private void buttonSaveDestinationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveDestinationActionPerformed

        if (textNameDestination.getText().isEmpty() || textTypeDestination.getText().isEmpty()
                || textCostDestination.getText().isEmpty()) {
            JOptionPane.showMessageDialog(userInterface, "Please enter all fields!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Destination newDestination = checkNewDestination();
            if (newDestination != null) {
                destinationManager.addDestination(newDestination);
                setVisible(false);
                userInterface.getContentPane().repaint();
                textNameDestination.setText("");
                textCostDestination.setText("");
                textTypeDestination.setText("");
            }
        }
    }//GEN-LAST:event_buttonSaveDestinationActionPerformed


