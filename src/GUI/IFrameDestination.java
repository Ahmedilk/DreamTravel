    @Override
    public void tableChanged(TableModelEvent e) {
        int row = e.getFirstRow();
        Destination newDestination = destinationManager.getDestination(row);
        try {
            destinationManager.updateDestination(newDestination);
        } catch (Exception exc) {
            JOptionPane.showMessageDialog(this,
                    exc,
                    "Please Correct",
                    JOptionPane.ERROR_MESSAGE);
        }
        getContentPane().repaint();
    }
    
