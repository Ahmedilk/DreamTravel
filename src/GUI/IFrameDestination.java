    private void buttonDeleteDestinationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteDestinationActionPerformed
        int pos = tableDestinationManager.getSelectedRow();
        int max = tableDestinationManager.getRowCount();
        if (pos < 0 || pos >= max) {
            return;
        }
        Destination destination = destinationManager.getDestination(pos);
        int option = JOptionPane.showConfirmDialog(this, "" + destination, "Really delete this destination?", JOptionPane.YES_NO_OPTION);
        if (option != JOptionPane.YES_OPTION) {
            return;
        }
        destinationManager.deleteDestination(destination);
        getContentPane().repaint();
    }//GEN-LAST:event_buttonDeleteDestinationActionPerformed

