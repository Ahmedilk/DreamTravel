    private void buttonSaveTripActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveTripActionPerformed
        if (textNameTrip.getText().isEmpty() || textTypeTrip.getText().isEmpty()
                || textCostTrip.getText().isEmpty() || selectedDuration == null) {
            JOptionPane.showMessageDialog(userInterface, "Please enter all fields!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Trip newTrip = checkNewTrip();
            if (newTrip != null) {
                tripManager.addTrip(newTrip);
                setVisible(false);
                userInterface.getContentPane().repaint();
                textNameTrip.setText("");
                textCostTrip.setText("");
                textTypeTrip.setText("");
                checkBoxSieben.setSelected(false);
                checkBoxVierzehn.setSelected(false);
            }
        }
    }//GEN-LAST:event_buttonSaveTripActionPerformed

