    private Trip checkNewTrip() {
        Trip newTrip;
        int tripID = tripManager.getNextTripID();
        // 9000 IDs is the maximum, which can be stored
        if (tripID > 9000) {
            JOptionPane.showMessageDialog(this, "Maximum Limit of trips!", "Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        String tName = textNameTrip.getText().trim();
        String tType = textTypeTrip.getText().trim();
        Double tCost;
        textNameTrip.setBackground(new Color(255, 255, 255));
        textTypeTrip.setBackground(new Color(255, 255, 255));
        textCostTrip.setBackground(new Color(255, 255, 255));

        if (!isValidTName(tName)) {
            JOptionPane.showMessageDialog(userInterface, "Only letters are allowed, and two consecutive spaces are not allowed.\nAnd/Or incorrect characters", "Invalid Name!", JOptionPane.ERROR_MESSAGE);
            textNameTrip.setBackground(new Color(255, 102, 102));
            return null;
        }

        if (!isValidTType(tType)) {
            JOptionPane.showMessageDialog(userInterface, "Only letters are allowed, and two consecutive spaces are not allowed.\nAnd/Or incorrect characters", "Invalid Type!", JOptionPane.ERROR_MESSAGE);
            textTypeTrip.setBackground(new Color(255, 102, 102));
            return null;
        }
        
        try {
            tCost = Double.valueOf(textCostTrip.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "The cost field should exclusively consist of numerical values!\nPlease correct", "Invalid Cost!", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (tCost <= 0) {
            JOptionPane.showMessageDialog(this, "Cost is too low!\nEnter a positive value under 10000!", "Invalid Cost!", JOptionPane.ERROR_MESSAGE);
            return null;
        } else if (tCost > 10000) {
            JOptionPane.showMessageDialog(this, "Cost is too high!\nEnter a positive value under 10000!", "Invalid Cost!", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        Integer tDuration = selectedDuration;

        newTrip = new Trip(tripID, tName, tType, tCost, tDuration);
        return newTrip;
    }
    
    private boolean isValidTName(String tName) {
        return tName.matches("[A-Za-z]+(\\s[A-Za-z]+)*") && tName.length() >= 2 && tName.length() <= 20;
    }

    private boolean isValidTType(String tType) {
        return tType.matches("[A-Za-z]+(\\s[A-Za-z]+)*") && tType.length() >= 2 && tType.length() <= 20;
    }

